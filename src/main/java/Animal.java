public class Animal {

    private String AnimalType;
    private String AnimalName;

    public Animal(String animalType, String animalName) {
        AnimalType = animalType;
        AnimalName = animalName;
    }

    public String getAnimalType() {
        return AnimalType;
    }

    public String getAnimalName() {
        return AnimalName;
    }
}
